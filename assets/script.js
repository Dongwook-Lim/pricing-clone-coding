function buttonChange1(self) {
  if (self.textContent === "Sign up") {
    self.style.backgroundColor = "#039be5";
    self.style.color = "white";
    self.style.borderColor = "#039be5";
    self.textContent = "Done";
    alert("Are you sure?");
  } else {
    self.style.backgroundColor = "white";
    self.style.color = "#90caf9";
    self.style.borderColor = "#90caf9";
    self.textContent = "Sign up";
  }
}

